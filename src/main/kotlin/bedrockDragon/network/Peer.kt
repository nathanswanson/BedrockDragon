package bedrockDragon.network

import bedrockDragon.network.raknet.RakNet
import java.net.InetSocketAddress
import java.util.*

/**
 * Peer is an abstract class for a client or server
 * In the case of Bedrock Dragon it DragonServer
 * extends this class.
 * @author Nathan Swanson
 * @property bindAddress
 */
abstract class Peer(private val bindAddress: InetSocketAddress) {

    /**
     *
     */
    var isRunning = false
    /**
     *
     */
    var guid : Long = 0
    /**
     *
     */
    var mtu = RakNet.getMaximumTransferUnit()


    /**
     *
     */
    open fun start(): Boolean {
        val uuid = UUID.randomUUID()

        guid = uuid.mostSignificantBits
        mtu = RakNet.getMaximumTransferUnit(bindAddress)

        isRunning = true
        return true
    }

    /**
     *
     */
    open fun stop(): Boolean {
        isRunning = false
        return true
    }

    /**
     *
     */
    fun disconnect() {

    }

}