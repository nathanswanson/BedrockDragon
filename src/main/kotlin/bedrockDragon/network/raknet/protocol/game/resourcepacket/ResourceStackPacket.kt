package bedrockDragon.network.raknet.protocol.game.resourcepacket

import bedrockDragon.network.raknet.VarInt
import bedrockDragon.network.raknet.protocol.game.MinecraftPacketConstants
import bedrockDragon.network.raknet.protocol.game.PacketPayload
import bedrockDragon.network.raknet.protocol.game.type.resourcepack.ResourcePack

class ResourceStackPacket: PacketPayload(MinecraftPacketConstants.RESOURCE_PACK_STACK) {
    var forcedToAccept = false
    lateinit var behaviorPackEntry: Array<ResourcePack>
    lateinit var resourcePackEntry: Array<ResourcePack>
    var experimental = false
    var gameVersion = "1.17.40"


    override suspend fun encode() {
        writeBoolean(forcedToAccept)
        VarInt.writeUnsignedVarInt(behaviorPackEntry.size, outputStream)
        for(pack in behaviorPackEntry) {
            writeString(pack.packId.toString())
            writeString(pack.packVersion.contentToString())
            writeString("") //subpack
        }

        VarInt.writeUnsignedVarInt(resourcePackEntry.size, outputStream)
        for(pack in resourcePackEntry) {
            writeString(pack.packId.toString())
            writeString(pack.packVersion.contentToString())
            writeString("") //subpack
        }

        writeString(gameVersion)
        writeIntLE(0) //experiments length
        writeBoolean(experimental)
    }
}